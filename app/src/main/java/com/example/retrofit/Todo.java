package com.example.retrofit;

public class Todo {
    private int userId;
    private int id;
    private String title;
    private Boolean completed;

    public int getUserId() {
        return userId;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public Boolean getCompleted() {
        return completed;
    }

}
